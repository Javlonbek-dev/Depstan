                        <div class="entry-content" itemprop="text">

                            <p>Департаментнинг юқори турувчи органи Ўзбекистон Республикаси Президентининг 2018 йил 12
                                декабрдаги “Техник жиҳатдан тартибга солиш, стандартлаштириш, сертификатлаштириш ва
                                метрология тизимларини янада такомиллаштириш чора-тадбирлари
                                тўғрисида”ги 4059-сон қарорига мувофиқ  Ўзбекистон стандартлаштириш, метрология ва
                                сертификатлаштириш (“Ўзстандарт”)агентлиги ҳисобланади.  </p>


                            <p>&#171;Ўзстандарт&#187; агентлиги &nbsp;<a href="http://lex.uz/pdfs/4305020">Ўзбекистон
                                    Республикаси Вазирлар Маҳкамасининг 2019 йил 24 апрелдаги “Ўзбекистон
                                    стандартлаштириш, метрология ва сертификатлаштириш агентлигининг фаолиятини янада
                                    такомиллаштириш чора-тадбирлари тўғрисида” 348-сонли қарор</a>ига
                                асосан техник жиҳатдан тартибга солиш, стандартлаштириш, сертификатлаштириш ва
                                метрология соҳасидаги вазифаларни ҳал этиш учун махсус ваколатли давлат бошқаруви органи
                                ҳисобланади.</p>


                            <p>&#171;Ўзстандарт&#187; агентлиги Ўзбекистон Республикасининг стандартлаштириш, метрология
                                ва сертификатлаштириш бўйича миллий орган ҳисобланади ҳамда ўз фаолиятида Ўзбекистон
                                Республикаси Вазирлар Маҳкамасига ҳисобот беради.</p>


                            <p>&#171;Ўзстандарт&#187; агентлиги ўз фаолиятида Ўзбекистон Республикаси Конституциясига ва
                                қонунларига, Ўзбекистон Республикаси Олий Мажлиси палаталарининг қарорларига ва бошқа
                                қонун ҳужжатларига, Ўзбекистон Республикаси
                                Президентининг фармонлари, қарорлари ва фармойишларига, Ўзбекистон Республикаси Вазирлар
                                Маҳкамасининг қарорларига ва фармойишларига шунингдек, Низом ва бошқа қонунчилик
                                ҳужжатларига амал қилади.</p>


                            <p>&#171;Ўзстандарт&#187; агентлиги ва унинг идоравий мансуб ташкилотлари юридик шахс
                                ҳисобланади, Ўзбекистон Республикаси Давлат герби тасвири туширилган ва ўз номи ёзилган
                                муҳрга, банк муассасаларида ҳисоб-китоб ва бошқа
                                ҳисобрақамларига эга.</p>
