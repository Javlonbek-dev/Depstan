                            <h1 class="entry-title" itemprop="headline">Press-reliz</h1>
                        </header>


                        <div class="entry-content" itemprop="text">

                            <p><strong>“Oʻzstandart” agentligi huzurida tashkil etilgan Texnik jihatdan tartibga solish, standartlashtirish, sertifikatlashtirish va metrologiya sohasidagi qonunchilik talablariga rioya qilinishini taʼminlash departamenti faoliyati toʻgʻrisida</strong></p>



                            <p class="has-text-align-right"><strong><em><u>2019-yil 6-may Milliy matbuot uyi</u></em></strong></p>



                            <p>Maʼlumki Respublikamiz Prezidenti oʻzining har bir chiqishida, ishlab chiqarish, tadbirkorlik subyektlari bilan har bir uchrashuvida mamlakatimizni yana-da rivojlantirish, xalqimiz hayotini farovon qilishning yagona yoʻli
                                jahon miqyosida raqobatbardosh mahsulotlar ishlab chiqarish va xalqaro mehnat taqsimotida oʻz oʻrnimizni topa bilishimizda ekanligini takror va takror uqtiradilar. Yurtimizda amalga oshirilayotgan islohotlarning pirovard
                                maqsadi ham ushbu vazifalarni amalga oshirishga qaratilgan. Prezidentimiz va Respublika hukumatining “Oʻzstandart” agentligi tizimini takomillashtirishga qaratilgan qarorlari ham aslida mamlakatimizda jahon standartlariga
                                muvofiq boʻlgan mahsulotlar ishlab chiqarishni taʼminlashga yoʻnaltirilgan.</p>



                            <p>Oʻzbekiston Respublikasi Prezidentining 2018-yil 12-dekabrdagi PQ-4059 sonli qarori bilan “Oʻzstandart” agentligi huzurida Texnik jihatdan tartibga solish, standartlashtirish, sertifikatlashtirish va metrologiya sohasidagi
                                qonunchilik talablariga rioya qilinishini taʼminlash departament(keyingi oʻrinlarda &#8212; departament)i tashkil etildi va quyidagi vazifalar yuklatildi:</p>



                            <p>&#8212; Texnik jihatdan tartibga solish, standartlashtirish, sertifikatlashtirish va metrologiya sohasidagi qonun buzilishlarining oldini olish va profilaktika qilinishini amalga oshirish;</p>



                            <p>&#8212; Texnik jihatdan tartibga solish, standartlashtirish, sertifikatlashtirish va metrologiya masalalarida tadbirkorlik subyektlariga har tomonlama yordam koʻrsatish;</p>



                            <p>&#8212; Texnik reglamentlar, standartlashtirish boʻyicha normativ hujjatlar talablariga rioya qilinishi, oʻlchov birliklari yagonaligi va ishonchliligini taʼminlash majburiy sertifikatlashtirish qoidalari talablari bajarilishi
                                ustidan davlat nazoratini tashkilqilish va amalga oshirish.</p>



                            <p>Qarorda shuningdek, departament yuridik shaxs sifatida faoliyat yuritishi va uning faoliyati ustidan umumiy rahbarlik Agentlik zimmasiga yuklanishi belgilangan.</p>



                            <p>2019-yil 24-aprelda Oʻzbekiston Respublikasi Vazirlar Mahkamasining 348-son (Oʻzbekiston standartlashtirish, metrologiya va sertifikatlashtirish agentligining faoliyatini yana-da takomillashtirish toʻgʻrisida)qarori bilan
                                esa Departamentning Nizomi, meʼyoriy-huquqiy va moliyaviy asoslari va faoliyatining ustivor yoʻnalishlari belgilab berildi.</p>



                            <p>Ushbu yoʻnalishlar, bir ogʻiz soʻz bilan aytganda asosan tadbirkorlik subyektlariga meʼyoriy-huquqiy yordam koʻrsatish, ularga tovar ishlab chiqarish va xizmat koʻrsatishda yuzaga kelayotgan toʻsiqlarni bartaraf qilishga
                                qaratilgan.</p>



                            <p>Departament tashkiliy tuzilmasi ham ushbu vazifalarni amalga oshirish nuqtai-nazaridan shakllantirilgan boʻlib, texnik tartibga solish sohasidagi normativ hujjatlarga rioya etish, oʻlchov birliklari yagonaligini taʼminlash,
                                oziq-ovqat sifati va sertifikatlashtirish qoidalariga rioya qilinishini taʼminlash faoliyatning ustivor yoʻnalishlari hisoblanadi.</p>



                            <p>Shuni alohida taʼkidlash joizki, Departament sobiq viloyat standartlashtirish va metrologiya boshqarmalari negizida tashkil etilgan boʻlsada, mohiyati, faoliyat yoʻnalishlari jihatdan ulardan tubdan farq qiladi. Sobiq boshqarmalarda
                                asosiy yoʻnalish standart va boshqa meʼyoriy hujjatlar talablariga rioya qilinishini nazorat qilishdan iborat boʻlgan boʻlsa, departament mutaxassislarining asosiy vazifasi tadbirkorlarga ushbu meʼyoriy hujjatlar talablarini
                                bajarishda amaliy va uslubiy yordam koʻrsatishdan iborat etib belgilangan.</p>



                            <p>Bu yordam davra suhbatlari, seminarlar, konferensiya va muloqotlar oʻtkazish yoʻli bilan texnik tartibga solish, standartlashtirish, sertifikatlashtirish va metrologiya sohalarida qonun hujjatlari buzilishining oldini olish
                                va profilaktikasi ni amalga oshirishda, oʻtkazilgan tadbirlar, davra suhbatlari va muloqotlarni matbuot orqali yoritishda va boshqalarda oʻz ifodasini topadi. Shuningdek ochiq eshiklar oʻtkazish orqali zarur axborot
                                va maʼlumotlar tadbirkorlarga yetkaziladi, tarqatma materiallar tayyorlanib, tarqatiladi. Tadbirkorlik subyektlari oʻrtasida huquqiy ong, huquqiy madaniyatni oshirish va qonun ustuvorligini mustahkamlashga qaratilgan
                                huquqiy targʻibot tadbirlarida ishtirok etadi.</p>



                            <p>Texnik jihatdan tartibga solish, metrologik qoidalar va sertifikatlashtirish tartiblari sohalarida normativ hujjatlarni ishlab chiqarishda amaliy yordam koʻrsatadi, rus va oʻzbek tillariga tarjima qiladi.</p>
