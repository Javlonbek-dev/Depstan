                        <div class="entry-content" itemprop="text">

                            <p>Yogʻ-moy mahsulotlari asosiy kundalik ehtiyoj mahsulotlaridan boʻlib, dasturxonimiz
                                toʻkinchiligi unsurlaridan biri hisoblanadi. Shunday ekan, ularning sifati muhim
                                ahamiyatga ega. Respublikamizda hozirda 240 dan ortiq
                                yogʻ-moy mahsulotlari ishlab chiqaruvchi korxonalar faoliyat koʻrsatmoqda. Ularning
                                asosiy xom ashyosi paxta chigiti hisoblanib, yiliga oʻrtacha 1,2 million tonna texnik
                                paxta chigiti qayta ishlanib, 200 ming tonna
                                atrofida paxta moyi ishlab chiqariladi. Bundan tashqari, yiliga oʻrta hisobda 200-250
                                ming tonna soya va kungaboqar donlari import qilinib, 80 ming tonna atrofida oʻsimlik
                                moyi aholi isteʼmoli uchun yetkazib berilmoqda.
                                Ishlab chiqarilayotgan yogʻ-moy mahsulotlari hajmi yildan-yilga oshib borishiga
                                qaramasdan, aholi soni va sanoat ehtiyojlarining oʻsishi natijasida oʻsimlik moylariga
                                boʻlgan talab ham ortib bormoqda va shu bois yiliga
                                150-180 ming tonna tayyor oʻsimlik moyi import qilinmoqda.</p>


                            <p>Quyida xalqimiz tomonidan sevib isteʼmol qilinadigan asosiy yogʻlar toʻgʻrisida toʻxtalib
                                oʻtamiz.</p>


