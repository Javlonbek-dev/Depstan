                            <p>Mamlakatimizga soʻnggi yillarda katta miqdorlarda import qilinayotgan oʻsimlik
                                yogʻlaridan yana biri bu – palma yogʻidir. Palma yogʻi biz uchun qaysidir maʼnoda
                                yangilik boʻlsada, insoniyat tomonidan ishlab chiqarishga
                                qoʻyilgan eng qadimiy yogʻ hisoblanadi. Ilk bor palma yogʻi gʻarbiy Afrika aholisi
                                tomonidan 5 ming yilcha burun ishlab chiqarilgan boʻlsada, tez orada qadimgi Misrda ham
                                ommalashib ketdi. 18 asr boshlarida palma yogʻi
                                yevropaga keltirilib, aholiga taʼmi, foydali xususiyatlari, eng muhimi nisbatan
                                arzonligi bilan manzur boʻldi. Hozirda palma yogʻi dunyoda eng ommalashgan yogʻ
                                hisoblanib, asosan Malayziya va Indoneziyada tayyorlanib,
                                butun jahon boʻylab eksport qilinadi.</p>


                            <p>Palma yogʻi tarkibida karotinoidlar, fosfolipidlar, Ye, D va K vitaminlari hamda magniy,
                                fosfor kabi boshqa foydali mineral moddalarning mavjudligi uning foydalilik
                                xususiyatlarini belgilaydi.</p>


                            <p>Palma yogʻi oshqozon-ichak tizimi faoliyatini yaxshilaydi, jigarda yogʻ toʻplanishiga
                                qarshilik qiladi, oshqozon va ichaklardagi yaralar bitishiga koʻmaklashadi.</p>


                            <p>Palma yogʻi bugungi kunda pishiriqlar, konditer mahsulotlari tayyorlashda, tarkibida
                                sariyogʻ ishlatiladigan yoki boʻlishi shart boʻlgan boshqa oziq-ovqat
                                mahsulotlari(quyultirilgan sut, qaymoq va hatto suzma va b.)da keng
                                ishlatilmoqda.</p>


                            <p>Palma yogʻini xomilador, yosh bolali, allergiyaga beriluvchan kishilar ehtiyot boʻlib
                                isteʼmol qilishlari zarur. Bu yogʻning asosiy zarari tarkibida toʻyingan yogʻlarning
                                koʻp boʻlishidadir. Qator olimlar palma yogʻini
                                koʻp miqdorda isteʼmol qilish yurak-tomir kasalliklarini keltirib chiqaradi, deb
                                hisoblashadi. Shuningdek, palma yogʻi qiyin eruvchanligi sababli, organizmdan
                                chiqarilishi qiyin kechadi va ichak, tomirlarda qolib ketishi
                                mumkin. Buning ustiga bu yogʻ kanserogen hisoblanib, saraton chaqirish xavfiga ham ega,
                                deyishadi baʼzi tadqiqotchilar.</p>


                            <p>Biz esa, aziz mushtariy, yana oʻsha oʻzimiz yoqtirgan jumlamizga qaytamiz. Meʼyor. Ushbu
                                yogʻlarning barchasini belgilangan meʼyorlarga rioya qilgan holda isteʼmol qilsangiz,
                                albatta faqatgina foydali xususiyatlaridan bahramand
                                boʻlasiz.</p>


                            <p>Ha, aytganday, mamlakatimizda amalda boʻlgan “Texnik jihatdan tartibga solish
                                toʻgʻrisida”gi Qonunga asosan, “Yogʻ-moy mahsulotlarining xavfsizligi toʻgʻrisida”
                                umumiy texnik reglament qabul qilingan.</p>


                            <p>Ushbu texnik reglamentda yogʻ-moy mahsulotlarini ishlab chiqarish jarayoni,
                                tamgʻalanishi, tashilishi va saqlanishiga jami 33 xil turdagi xavfsizlik meʼyorlari
                                belgilangan boʻlib, ular orasida turli muhokamaga sababchi
                                boʻlayotgan koʻrsatgichlardan biri bu – yogʻ-moy mahsulotlari tarkibidagi “Moy
                                kislotalarining trans izomerlari” hisoblanadi.</p>


                            <p>Texnik reglamentda oʻsimlik moylari, hayvon yogʻlari, baliq yogʻlarini qayta ishlashda
                                hosil boʻlgan margarinlar, maxsus yogʻlar, qandolat yogʻlari, nonvoychilik yogʻlari,
                                bundan tashqari sut yogʻini oʻrnini bosuvchilar,
                                kakao moyi ekvivalentlari yogʻ-moy mahsulotlari uchun “Moy kislotalarining trans
                                izomerlari” xavfsizlik normalari belgilangan boʻlib, ichki bozorda ushbu normalarga
                                javob bermaydigan mahsulotlar sotilishiga yoʻl qoʻyilmaydi.
                            </p>


                            <p> “Oʻzstandart” agentligi huzuridagi Departament mutaxassislari tomonidan yurtimizda
                                sotilayotgan yogʻ-moy mahsulotlarining amaldagi normativ xujjatlarga muvofiqligi
                                oʻrganib boriladi. Oʻtgan yil adogʻida oʻtkazilgan xuddi
                                shunday oʻrganishlardan birida Rossiya Federatsiyasida ishlab chiqarilgan, yogʻdorligi
                                72 foiz boʻlgan qaymoq tamli “Shedroye leto” va yogʻdorligi 60 foiz, qaymoq tamli
                                “Saratovskiy” margarinlari Respublika sinov va
                                sertifikatlashtirish markazi davlat korxonasi qoshidagi “Kimyo-biologiya” sinov
                                laboratoriyasida tahlil qilindi.</p>


                            <p>Tahlil natijasida “Saratovskiy” margarinining belgilangan normalarga muvofiqligi
                                tasdiqdangan boʻlsa, “Shedroye leto” esa yogʻning erish harorati va tranizomerlarning
                                massaviy ulushi koʻrsatkichlari boʻyicha amaldagi normativ
                                hujjatlar talablariga javob bermasligi maʼlum boʻldi. </p>


                            <p> Xulosa sifatida shuni aytish mumkinki, aziz mushtariylarimiz ham bozor va doʻkonlardagi
                                har bir xarididan oldin albatta, ushbu mahsulot uchun muvofiqlik sertifikatini talab
                                qilishlari jamoatchilik nazoratini kuchaytirish
                                bilan birga, ishlab chiqarilayotgan va sotilayotgan mahsulotlarning toza va sifatli
                                boʻlishiga xizmat qiladi.</p>


