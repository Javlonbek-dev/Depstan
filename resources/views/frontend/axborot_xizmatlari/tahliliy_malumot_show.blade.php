                            <h1 class="entry-title" itemprop="headline">Seryogʻlilik sifat koʻrsatkichimi?</h1>
                            <div class="entry-meta">
                                <span class="posted-on"><time class="entry-date published"
                                                              datetime="2020-04-22T09:00:00+05:00"
                                                              itemprop="datePublished">22.04.2020</time></span></div>
