                            <p>Mamlakatimizda soʻnggi yillarda kungaboqar yogʻi ham ommalashib bormoqda. Kungaboqar
                                yogʻi tarkibida qator toʻyinmagan kislotalar, A, Ye, va D vitaminlarining mavjudligi
                                uning foydali xususiyatlarini belgilaydi. Toʻyinmagan
                                kislotalar organizmda asab tolalari vujudga kelishida faol ishtirok etadi, organizmdan
                                yomon xolesterinlarni chiqarishda ishtirok etadi, infark, ateroskleroz va boshqa
                                yurak-tomir kasalliklari oldini olish vositasi
                                sifatida koʻriladi.</p>


                            <p>Kungaboqar yogʻining zarari uni qizdirishda hosil boʻladigan xavfli kanserogen moddalarda
                                namoyon boʻladi. Kanserogen moddalar taomlarni qovurish jarayonida vujudga keladi va
                                saraton kabi xavfli dardlar bilan ogʻrishga
                                olib kelishi mumkin. Shuni alohida taʼkidlash joizki, yogʻni qayta qizdirishda
                                kanserogen moddalar bir necha barobar koʻp miqdorda hosil boʻlishi aniqlangan. <strong>Shu
                                    bois yogʻ qizdirgan tovangizni har safar tozalab yuvish tavsiya qilinadi.</strong>
                            </p>


