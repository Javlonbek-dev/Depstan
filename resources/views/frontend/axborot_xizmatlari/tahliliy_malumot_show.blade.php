                            <p>Xalqimiz tomonidan oqmoy deb yuritiladigan paxta yogʻi tarkibida turli foydali elementlar
                                – V, Ye, RR vitaminlari, Omega 3 va Omega 6 toʻyinmagan yogʻlari, koʻp miqdorda
                                miristin, palmitin, stearin, araxin, oleain va boshqa
                                kislotalarning mavjudligi bilan boshqa yogʻlardan ustun turadi. Paxta yogʻi yuqori
                                kaloriyali boʻlib, 100 gramm mahsulotda 884 kaloriya mavjud.</p>


                            <p>Paxta yogʻi inson organizmi uchun foydali boʻlgan moddalar bilan toʻyinganligi bois,
                                qator kasalliklarning oldini olishda muhim oʻrin tutadi. Paxta yogʻi, immunitetga ijobiy
                                taʼsir qiladi, asab tizimini mustahkamlaydi,
                                organizmni yoshartiradi, xolesterin darajasini pasaytiradi, yurak toʻqimalari va
                                tomirlarining elastikligini oshiradi va hokazo.</p>


                            <p>Shu bilan birga chigitdan paxta yogʻi olinishi jarayonida maʼlum darajada yogʻ atomidagi
                                vodorodning molekula zanjirida qarama-qarshi tomonga koʻchib oʻtishi oqibatida
                                transyogʻlar vujudga kelishi, ularning erish darajasi
                                inson organizmi harorati(36,6<sup>o</sup>)dan yuqori(45<sup>o</sup>)ligi tufayli uni
                                koʻp isteʼmol qilish zararli ekanligi toʻgʻrisida ham tadqiqotlar mavjud.</p>


                            <p>Oqmoy sifatining yana bir, oddiy koʻz bilan aniqlanadigan jihati uning rangi hisoblanadi.
                                Paxta yogʻi ishlab chiqarilishi jarayonida tarkibidagi gossipol moddasidan tozalanishi
                                zarur. <strong>Gossipol oʻzining mavjudligini yogʻga jigarrang-qoramtir rang berib
                                    namoyon qiladi.</strong> Tozalangan oqmoy sargʻimtir och qizgʻish boʻlishi kerak.
                                Gossipol olifa va solidol ishlab chiqarishda xom ashyo hisoblanadi.</p>


